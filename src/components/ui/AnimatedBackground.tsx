export function TypewriterText({ text, className }: { text: string; className?: string }) {
  return (
    <motion.h1
      className={className}
      initial={{ opacity: 1 }}
      animate={{ opacity: 1 }}
    >
      {text.split('').map((char, index) => (
        <motion.span
          key={index}
          initial={{ opacity: 0 }}
          animate={{ opacity: 1 }}
          transition={{
            duration: 0.05,
            delay: index * 0.05,
          }}
        >
          {char}
        </motion.span>
      ))}
    </motion.h1>
  )
}